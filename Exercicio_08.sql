-- QUESTAO 12
SELECT
LOCACAO.IDLOCACAO,
TABELA.VALOR AS VALOR_KM,
ROUND(LOCACAO.KM_ENTREGA - LOCACAO.KM_LOCACAO,2) AS KM_LOCACAO,
ROUND((LOCACAO.KM_ENTREGA - LOCACAO.KM_LOCACAO)* (TABELA.VALOR), 2) AS VALOR_TOTAL_LOCACAO
FROM 
LOCACAO 
INNER JOIN VEICULO ON 
VEICULO.IDVEICULO=LOCACAO.IDVEICULO
INNER JOIN TABELA_VEICULO ON
TABELA_VEICULO.IDVEICULO=VEICULO.IDVEICULO
INNER JOIN TABELA ON
TABELA_VEICULO.IDTABELA=TABELA.IDTABELA
WHERE TABELA.TIPO ='KM'
GROUP BY
LOCACAO.IDLOCACAO,
TABELA.VALOR;

-- QUESTAO 13
SELECT
LOCACAO.IDLOCACAO,
TABELA.VALOR AS VALOR_DIA,
DATEDIFF(LOCACAO.DT_ENTREGA, LOCACAO.DT_LOCACAO) AS DIAS_LOCACAO,
ROUND(DATEDIFF(LOCACAO.DT_ENTREGA, LOCACAO.DT_LOCACAO)*TABELA.VALOR, 2) AS VALOR_TOTAL
FROM LOCACAO
INNER JOIN VEICULO ON 
VEICULO.IDVEICULO=LOCACAO.IDVEICULO
INNER JOIN TABELA_VEICULO ON
TABELA_VEICULO.IDVEICULO=VEICULO.IDVEICULO
INNER JOIN TABELA ON
TABELA_VEICULO.IDTABELA=TABELA.IDTABELA
WHERE TABELA.TIPO ='DIA'
GROUP BY 
LOCACAO.IDLOCACAO,
TABELA.VALOR;



